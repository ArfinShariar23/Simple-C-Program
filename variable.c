//Variable in C 
#include<stdio.h>
int main()
{
    int age = 10;  // age is a variable where value is age
    char ch = 'a'; // ch is a variable where value is 'a'
    double mark = 10.56; //mark is a variable where value is 10.56
    
    printf("Your Age is = %d\n",age);
    printf("Your Favourite Character = %c\n",ch);
    printf("Your Last Exam Mark = %lf\n",mark);
    return 0;
}